    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        // TODO add your handling code here:
        String nome = textNome.getText();
        String codigo = textCodigo.getText();
        String stringSalario = textSalario.getText();
        boolean isNumeric =  stringSalario.matches("[+-]?\\d*(\\.\\d+)?");
        String nivel = (String) comboNivel.getSelectedItem();
        String funcao = (String) comboFuncao.getSelectedItem();
        String stringDep = (String) comboDepartamento.getSelectedItem();
        
        if(stringDep.equals("Matemática")){
            this.dep = 0;
        } else if(stringDep.equals("Biologia")){
            this.dep = 1;
        } else if(stringDep.equals("Geografia")){
            this.dep = 2;
        } else this.dep = 3;
        boolean preenchidos = false;
        if(!nome.equals("") && !codigo.equals("") && !stringSalario.equals("") && !funcao.equals("") && !nivel.equals("") && isNumeric) preenchidos = true;
        if(preenchidos){
            double salario = Float.parseFloat(textSalario.getText());
            fun = new Tecnico(codigo, nome, salario, nivel, funcao);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos corretamente");
        }
    }//GEN-LAST:event_btnCadActionPerformed

